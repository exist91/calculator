
public class Calculater {

	int MyAdd(int operand1, int operand2)
	{
		return operand1 + operand2;
	}
	
	int MySub(int operand1, int operand2)
	{
		return operand1 - operand2;
	}
	
	int MyMult(int operand1, int operand2)
	{
		return operand1 * operand2;
	}
	
	float MyDiv(float operand1, float operand2)
	{
		return operand1 / operand2;
	}
	
}
